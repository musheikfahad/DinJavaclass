package com.Inheritance;

// single inheritance

public class Parent_singleInheritance {

	public  void bike()  {
		
		System.out.println("bajaj");
		

	}
	public void car() {
		
	System.out.println("tesla");
	}
	
	public static void main(String[] args) {
	Parent_singleInheritance obj = new Parent_singleInheritance();
		obj.bike();
		obj.car();
	}

	
}
